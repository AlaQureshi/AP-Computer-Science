public class Pizza {
    String size;
    String sauceType;
    int howMany;
    String meatType;
    String veggieType;
    public Pizza(String sz, String st, int hm, String mt, String vt){
        size = sz;
        sauceType = st;
        howMany = hm;
        meatType = mt;
        veggieType = vt;
    }
}
